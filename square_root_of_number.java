package project_01;
import java.util.*;
public class square_root_of_number {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int n;
		System.out.print("Enter any number-");
		n=sc.nextInt();
		double m=Math.sqrt(n);
		System.out.println("Square root="+m);
	}

}
